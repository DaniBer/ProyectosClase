package _03ejercicios;

public class _02Ejercicio {
    public static void main(String args[]){
        System.out.println("Hola mundo mundial");
        
        //escribiendo varias instrucciones en la misma linea el programa sigue ejecutandose
        System.out.println("Hola mundo"); System.out.println("Hola al resto");
        
        /*"Rompiendo" una instrucción el programa también se ejecuta dependiendo por 
        donde cortes la instrucción*/
        
        System.out
                .println("Hola mundo");
        
        /*El comportamiento de java es el crear una excepción sin darle un valor
        por defecto
        */
              
        int b;
        System.out.println(b);
    }
}
