package _02ejemplos;

public class _05VariablesYLiterales {
    public static void main(String args[]){
        //declarar variable
        int a;
        
        //declarar inicializar
        int b = 5;
        
        //declarar varias del mismo tipo
        double c, d, e = 5.0;
        long f, g;
        float h, i;
        
        //Literales
        a = 5;
        f = 5;
        //f = 9995546454545464;
        f = 9995546454545464L;
        
        c = 2.5;
        d = 2.5E200;
        
        //h = 2.5;
        h = 2.5F;
    }
}
